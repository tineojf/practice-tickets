    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if (!Tools.verifiedTextNotEmpty(listField)) {
            return;
        }

        try {
            int id = Integer.parseInt(fieldID.getText());
            CompanyModel companiaRegistrado = CompanyDAO.findByID(id);

            if (companiaRegistrado == null) {
                CompanyModel companiaNuevo = new CompanyModel(
                        fieldID.getText(),
                        fieldCompany.getText());
                CompanyDAO.create(companiaNuevo);
                JOptionPane.showMessageDialog(null, "Compañia registrada exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                this.loadData();

            } else {
                JOptionPane.showMessageDialog(null, "Error: ID registrado.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            System.err.println("Error al parsear el texto a entero: " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Error: El texto no es un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (!Tools.verifiedTextNotEmpty(listField)) {
            return;
        }

        try {
            int id = Integer.parseInt(fieldID.getText());
            CompanyModel companiaRegistrado = CompanyDAO.findByID(id);

            if (companiaRegistrado != null) {
                CompanyModel companiaNuevo = new CompanyModel(
                        String.valueOf(id),
                        fieldCompany.getText());
                CompanyDAO.update(id, companiaNuevo);
                JOptionPane.showMessageDialog(null, "Compañia actualizado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                this.loadData();

            } else {
                JOptionPane.showMessageDialog(null, "Error: ID no encontrado.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            System.err.println("Error al parsear el texto a entero: " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Error: El texto no es un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

