    private void loadData() {
        // load id in Select Field
        ArrayList<String> listaID = CompanyDAO.findOnlyID();
        selectSearch.removeAllItems();

        for (String item : listaID) {
            selectSearch.addItem(item);
        }

        // load data in jtable
        ArrayList<CompanyModel> listaCompany = CompanyDAO.findAll();
        DefaultTableModel tabla = new DefaultTableModel();
        tabla.addColumn("ID");
        tabla.addColumn("Compañia");

        for (CompanyModel company : listaCompany) {
            tabla.addRow(new Object[]{company.getId(), company.getName()});
        }

        this.table.setModel(tabla);
    }

